function RunTests()
	{
	var Result = '';
	
	for (var i = 0; i < Object.keys(TestCases_PrivateKeyToAddress).length; i++)
		{
		var CoinType = Object.keys(TestCases_PrivateKeyToAddress)[i];
		var Tests = TestCases_PrivateKeyToAddress[CoinType].tests;
		
		var Total = Tests.length;
		var Success = 0;
		
		for (var j = 0; j < Total; j++)
			{
			var Key = Tests[j][0];
			var ExpectedAddress = Tests[j][1];
			
			var ActualAddress = GetAddressFromKeyUnknown(CoinType, Key, false);
			var ActualAddress_Compressed = GetAddressFromKeyUnknown(CoinType, Key, true);
			
			if (ExpectedAddress == ActualAddress || ExpectedAddress == ActualAddress_Compressed)
				{
				Success++;
				}
			else
				{
				Result += 'Failed ' + CoinInfo[CoinType].fullName + ' test: ' + 
					Key + '\r\nExpected: ' + 
					ExpectedAddress + '\r\nActual: ' + 
					ActualAddress + '\r\n';
				}
			}	
			
		Result += 'Passed ' + Success + ' / ' + Total + ' '  + CoinInfo[CoinType].fullName + ' tests.\r\n';
		}
		
	return Result;
	}